import UIKit

extension UIFont {
    class var largeTitle: UIFont {
        return UIFont.systemFont(ofSize: 50.0, weight: .bold)
    }

    class var tempTitle: UIFont {
        return UIFont.systemFont(ofSize: 100.0, weight: .bold)
    }

    class var description: UIFont {
        return UIFont.systemFont(ofSize: 16.0)
    }
}
